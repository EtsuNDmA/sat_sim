function [B] = mf1wire(C,D,M,I)
%mf1wirwe ¬ычисл€ем магнитное поле пр€мого проводника (от точки — до D) в
%точке ћ
% C, D - векторы-строки координат начала и конца проводника, начало там,
% откуда течет ток, (м)
% M - интересующа€ нас точка, (м)
% I - ток в проводнике, (ј)
% B - вектор магнитной индукции, (“л)
mu0=1.25663706e-6;
l=D-C;
d=M-C-l*(l*(M'-C'))/norm(l)^2;
B=mu0*I/4/pi*cross(l,d)*(l*((M'-C')/norm(M'-C')-(M'-D')/norm(M'-D'))/(l*l')/(d*d'));
end

